    faq_id = f'FAQ_{station_prefix}_{seq:03d}'
    ans_id = f'ANSWER_{station_prefix}_{seq:03d}'
    
    # 问题实体
    faq_entity = {
        "id": faq_id,
        "type": "faq",
        "name": q_text,
        "name_cn": q_text,
        "properties": {
            "source": station,
            "seq": seq
        }
    }
    
    # 答案实体
    ans_entity = {
        "id": ans_id,
        "type": "faq_entity",
        "name": a_text[:100] if len(a_text) > 100 else a_text,
        "name_cn": a_text[:100] if len(a_text) > 100 else a_text,
        "properties": {
            "source": station,
            "full_answer": a_text,
            "seq": seq
        }
    }
    
    # 关系：q → a
    rel = {
        "id": f"rel_{faq_id}_{ans_id}",
        "source": faq_id,
        "target": ans_id,
        "type": "",
        "properties": {
            "note": f"{station}FAQ问答"
        }
    }
    
    ents.append(faq_entity)
    ents.append(ans_entity)
    rels.append(rel)
    
    # 加上tag
    tag_entity = {
        "id": f"TAG_{station_prefix}",
        "type": "faq_tag",
        "name": station,
        "properties": {}
    }
    tag_exists = any(e['id'] == f"TAG_{station_prefix}" for e in ents)
    if not tag_exists:
        ents.append(tag_entity)
    
    return seq

