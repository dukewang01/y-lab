def load_faq():
    with open(FAQ_PATH, encoding='utf-8') as f:
        return json.load(f)

def save_faq(data):
    with open(FAQ_PATH, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

def add_qa(data, q_text, a_text, station):
    ents = data['entities']
    rels = data['relationships']
    station_prefix = station.upper()
    existing = [e for e in ents if e['id'].startswith(f'FAQ_{station_prefix}_')]
    seq = len(existing) + 1
    faq_id = f'FAQ_{station_prefix}_{seq:03d}'
    ans_id = f'ANSWER_{station_prefix}_{seq:03d}'
    ents.append({
        "id": faq_id, "type": "faq", "name": q_text, "name_cn": q_text,
        "properties": {"source": station, "seq": seq}
    })
    ents.append({
        "id": ans_id, "type": "faq_entity",
        "name": a_text[:100] if len(a_text) > 100 else a_text,
        "name_cn": a_text[:100] if len(a_text) > 100 else a_text,
        "properties": {"source": station, "full_answer": a_text, "seq": seq}
    })
    rels.append({
        "id": f"rel_{faq_id}_{ans_id}", "source": faq_id, "target": ans_id,
        "type": "", "properties": {"note": f"{station}FAQ问答"}
    })
    tag_id = f"TAG_{station_prefix}"
    if not any(e['id'] == tag_id for e in ents):
        ents.append({"id": tag_id, "type": "faq_tag", "name": station, "properties": {}})
    return seq

