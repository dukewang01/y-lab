for e in all_events:
    safe_date = e['date'].replace('-', '')
    short_eng = (e['english_name'][:20] if e['english_name'] else e['chinese_name'][:10]).replace('/', '_').replace(' ', '_').replace('"', '').replace("'", '')
    short_eng = re.sub(r'[^a-zA-Z0-9_\u4e00-\u9fff]', '_', short_eng)
