owners_list = ["Elaine He", "Luisa Liu", "Lily Xie", "Nico Lin", "Cecily Hong", "Catherine Xu"]

for iso_date, count, content in event_blocks:
    lines = content.split('\n')
    for line in lines:
        line = line.strip()
        if not line or line.startswith('Event Start Date:') or line.startswith('Grand Totals') or line.startswith('Confidential') or line.startswith('Copyright'):
            continue
        
        status = 'Definite'
        if 'Tentative' in line: status = 'Tentative'
        elif 'Prospect' in line: status = 'Prospect'
        
        time_match = re.findall(r'(\d{1,2}:\d{2})\s+(\d{1,2}:\d{2})', line)
        start_time = time_match[0][0] + ':00' if time_match else ''
        end_time = time_match[0][1] + ':00' if len(time_match) > 0 else ''
        
        room_match = re.search(rooms_pattern, line)
        room = room_match.group(0) if room_match else ''
        if 'All Day Dining' in line: room = 'All Day Dining OPEN'
        if 'Coffee Break' in line: room = line.split('Coffee Break')[0].strip()
        if 'Bar Beer' in line: room = 'Bar Beer Society'
        if 'Italian Restera' in line: room = 'Italian Restaurant BACIO'
        
        # Find the BEO number / Definite marker etc to split Chinese + English names
        beo_num = ''
        beo_match = re.search(r'(\d{4})[\s-]*(?:BEO|Definite)', line)
        if beo_match: beo_num = beo_match.group(1)
        
        # Try to extract Chinese name
        chn_name = ''
        eng_name = ''
        
        # Extract event name: look for Chinese chars before the time/room
        # Strategy: find the time pattern, then everything before the first time
        time_start_positions = [m.start() for m in re.finditer(r'\b\d{1,2}:\d{2}\b', line)]
        if time_start_positions:
            before_time = line[:time_start_positions[0]].strip()
            # Chinese chars before the time
            chn_chars = re.findall(r'[\u4e00-\u9fffгҖҠгҖӢвҖ”\-]+', before_time)
            chn_name = ' '.join(chn_chars) if chn_chars else ''
            # English after Chinese / before first time
            rest_before = before_time
            for c in chn_chars: rest_before = rest_before.replace(c, '').strip()
            eng_name = rest_before.strip()
        
        # Owner
        owner = ''
        for o in owners_list:
            if o in line: owner = o; break
        
        if chn_name or eng_name:
            all_events.append({
                'date': iso_date, 'chinese_name': chn_name, 'english_name': eng_name,
                'start': start_time, 'end': end_time,
                'room': room,
                'status': status, 'owner': owner, 'beo': beo_num
            })

print(f"Parsed {len(all_events)} events")

