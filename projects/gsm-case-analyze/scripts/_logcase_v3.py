# ========== 从图谱加载场景 ==========
scene_entities = [e for e in risk['entities'] if e.get('category') == 'scene' and e['id'].startswith('SCENE_')]

# Build scene_index: word → list of scenes
scene_index = {}
for se in scene_entities:
    priority = se.get('priority', 99)
    name = se.get('label', '').replace('SCENE_', '')
    synonyms = se.get('synonyms', [se['id']])
    for syn in synonyms:
        if syn not in scene_index:
            scene_index[syn] = []
        scene_index[syn].append({
            'id': se['id'],
            'name': name,
            'priority': priority,
            'synonyms': synonyms
        })

# Build relation index
rel_from = {}
for r in risk['relations']:
    f = r.get('from', '')
    t = r.get('to', '')
    tp = r.get('type', '')
    if f not in rel_from:
        rel_from[f] = []
    rel_from[f].append({'to': t, 'type': tp, 'label': r.get('label', '')})

def expand_text(text):
    text = text.lower()
    expanded = set()
    words_in_text = set(re.findall(r'[\u4e00-\u9fffA-Za-z0-9]+', text))
    expanded.update(words_in_text)
    
    # Check synonyms
    for word, scenes in scene_index.items():
        if word in text:
            for scene in scenes:
                expanded.update(scene['synonyms'])
    
    return expanded

def match_score(case, complaint_expanded):
    text = (case.get('label','') + ' ' + case.get('description','')).lower()
    score = 0
    for syn in complaint_expanded:
        if syn in text:
            score += 1
    label_prefix = case.get('label','').lower()
    for syn in complaint_expanded:
        if syn in label_prefix:
            score += 2
    return score

def detect_scene(complaint_text, complaint_expanded):
    """
    多场景检测：返回所有匹配场景，按优先级排序。
    返回 list of (scene_id, scene_name, score)
    """
    matches = {}
    
    # Check each scene against complaint
    for se in scene_entities:
        sid = se['id']
        name = se.get('label','').replace('SCENE_','')
        priority = se.get('priority', 99)
        synonyms = se.get('synonyms', [])
        
        score = 0
        matched_syns = []
        for syn in synonyms:
            if syn in complaint_expanded:
                score += len(syn)  # longer match = higher confidence
                matched_syns.append(syn)
                if syn in complaint_text:
                    score += 3  # bonus for direct match
        
        # Only score if at least one synonym matched
        if score > 0:
            # Priority bonus for top-3 priority scenes
            # 儿童(pri=1):+15, 滑倒(pri=2):+10, 受伤(pri=3):+5
            # This ensures child safety overrides equally-matched scenes
            priority_boost = {1: 15, 2: 10, 3: 5}.get(priority, 0)
            score += priority_boost
            matches[sid] = {
                'id': sid,
                'name': name,
                'score': score,
                'priority': priority
            }
    
    # Sort by score descending, then priority ascending
    result = sorted(matches.items(), key=lambda x: (x[1]['score'], -x[1]['priority']), reverse=True)
    
    if not result:
        # Fallback: no match
        return None
    
    # Secondary sort: within same score, higher priority (lower number) comes first
    # Group by score, then sort groups by priority
    score_groups = {}
    for sid, info in result:
        s = info['score']
        if s not in score_groups:
            score_groups[s] = []
        score_groups[s].append(info)
    
    final = []
    for s in sorted(score_groups.keys(), reverse=True):
        group = sorted(score_groups[s], key=lambda x: x['priority'])
        for info in group:
            final.append((info['id'], info))
    
    # NO result that matched no synonyms — already filtered in loop
    return final

def get_recommended_cmps(scene_id):
    """Get CMP entities recommended for a scene"""
    result = []
    for rel in rel_from.get(scene_id, []):
        if rel['type'] == 'recommends':
            target = rel['to']
            for e in risk['entities']:
                if e['id'] == target:
                    result.append(e)
                    break
    return result

def get_related_scenes(scene_id):
    """Get related scenes"""
    result = []
    for rel in rel_from.get(scene_id, []):
        if rel['type'] == 'related_to':
            result.append(rel['to'])
    return result

