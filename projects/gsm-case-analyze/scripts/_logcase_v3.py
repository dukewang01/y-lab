# ========== SSOW图谱查询（动态读取图谱文件）==========
import json
import os

_SSOW_GRAPH_CACHE = None

def _load_ssow_graph():
    """加载RISK图谱的SSOW关联"""
    global _SSOW_GRAPH_CACHE
    if _SSOW_GRAPH_CACHE is not None:
        return _SSOW_GRAPH_CACHE
    
    graph_path = os.path.join(os.path.dirname(__file__), 'risk_graph.json')
    if not os.path.exists(graph_path):
        _SSOW_GRAPH_CACHE = {}
        return _SSOW_GRAPH_CACHE
    
    try:
        with open(graph_path, encoding='utf-8') as f:
            graph = json.load(f)
        
        entities = {e['id']: e for e in graph['entities']}
        relations = graph.get('relationships', [])
        
        # 构建 SCENE → SSOW 映射
        scene_ssow = {}
        for r in relations:
            if r.get('relation') == 'recommends':
                src = r['source']
                tgt = r['target']
                if tgt.startswith('SCENE_') and '_SSOW' in src:
                    if tgt not in scene_ssow:
                        scene_ssow[tgt] = []
                    ent = entities.get(src, {})
                    if ent:
                        scene_ssow[tgt].append(ent.get('label', ent.get('id', src)))
        
        _SSOW_GRAPH_CACHE = scene_ssow
        return scene_ssow
    except Exception:
        _SSOW_GRAPH_CACHE = {}
        return _SSOW_GRAPH_CACHE


